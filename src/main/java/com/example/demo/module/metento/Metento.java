package com.example.demo.module.metento;

public class Metento {
    private int a;
    private int b;

    public Metento(int a, int b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "Metento{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
